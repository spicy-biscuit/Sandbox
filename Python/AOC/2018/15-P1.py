Input = Input[0].split("\n")
Map = []

for x in Input:
  Map.append(list(x))

Width = len(Map[0])
Height = len(Map)

Units = []

for Row in range(Height):
  for Column in range(Width):
    if Map[Row][Column] in ["E", "G"]:
      Units.append((Map[Row][Column], 200, Row, Column))
      Map[Row][Column] = "."

def SortFunc(Unit):
  return(Unit[2] * 10000 + Unit[3])


while True:

  Units.sort(key = SortFunc)

  


def OutputMirror(Grid, Original):
  RowLength = len(Grid[0])
  ColumnLength = len(Grid)
  # print(RowLength, ColumnLength)

  #Check Vertical Mirrors
  Columns = []
  for x in range(RowLength):
    String = ""
    for y in range(ColumnLength):
      String += Grid[y][x]
    Columns.append(String)
  # print(Columns)
  # print("--")

  IsVerticalMirror = False
  for MirrorIndex in range(1, RowLength):
    if MirrorIndex == Original:
      continue
    IsRealMirror = True
    LeftMirrors = MirrorIndex
    RightMirrors = RowLength - MirrorIndex
    # print(LeftMirrors, RightMirrors)
    if RightMirrors > LeftMirrors:
      Sum = 2 * MirrorIndex - 1
      for x in range(LeftMirrors):
        if Columns[x] != Columns[Sum - x]:
          IsRealMirror = False
          break
    if LeftMirrors > RightMirrors:
      Sum = 2 * MirrorIndex - 1
      for x in range(RowLength - RightMirrors, RowLength):
        if Columns[x] != Columns[Sum - x]:
          IsRealMirror = False
          break
    if IsRealMirror:
      return(LeftMirrors)
      IsVerticalMirror = True
      break

  #Check Horizontal Mirrors
  IsHorizontalMirror = False
  for MirrorIndex in range(1, ColumnLength):
    if MirrorIndex * 100 == Original:
      continue
    IsRealMirror = True
    LeftMirrors = MirrorIndex
    RightMirrors = ColumnLength - MirrorIndex
    # print(LeftMirrors, RightMirrors)
    if RightMirrors > LeftMirrors:
      Sum = 2 * MirrorIndex - 1
      for x in range(LeftMirrors):
        if Grid[x] != Grid[Sum - x]:
          IsRealMirror = False
          break
    if LeftMirrors > RightMirrors:
      Sum = 2 * MirrorIndex - 1
      for x in range(ColumnLength - RightMirrors, ColumnLength):
        if Grid[x] != Grid[Sum - x]:
          IsRealMirror = False
          break
    if IsRealMirror:
      return(100 * LeftMirrors)
      IsHorizontalMirror = True
      break
  return("Empty")


